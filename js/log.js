$(document).ready(function(){
    // عند الضغط على الصنف الدي لديه الصنف remove قم بتنفيد الوظيفة التالية
    $(".remove").click(function(){
        //هنا قلنا له انه عند الضغط على الايقونة قم بحدف العنصر الاب والدي يملك الابناء والتي هي البطاقات دات الصنف card
        // فان لم نقم بهكدا تعليمة سوف يتم حدف جميع البطاقات لانها تعتبر ابناء 
        // اما التابع hide  فهو المسؤول عن عملية اخفاء البطاقة
        $(this).parents(".card").hide();
    });
});